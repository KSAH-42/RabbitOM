using System;

namespace RabbitOM.Net.Rtps
{
    /// <summary>
    /// Represent a message header seperator
    /// </summary>
    public enum RTSPSeparator
    {
        /// <summary>
        /// Comma (,)
        /// </summary>
        Comma = ',',

        /// <summary>
        /// SemiColon (;)
        /// </summary>
        SemiColon = ';',

        /// <summary>
        /// Slash (/)
        /// </summary>
        Slash = '/',
    }
}
